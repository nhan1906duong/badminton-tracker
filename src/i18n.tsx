const messages: Record<Locale, Record<TranslationKey, string>> = {
  en,
  vi,
}

type TranslationValues = Record<string, string | number | null | undefined>
export type TFunction = (key: TranslationKey, values?: TranslationValues) => string

interface LocaleContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: TFunction
}

function isLocale(value: unknown): value is Locale {
  return value === 'en' || value === 'vi'
}

function detectInitialLocale(): Locale {
  if (typeof window === 'undefined') return DEFAULT_LOCALE

  try {
    const saved = window.localStorage.getItem(LOCALE_STORAGE_KEY)
    if (isLocale(saved)) return saved
  } catch {
    // Storage can be unavailable in strict test/browser privacy contexts.
  }

  const browserLocale = window.navigator.language.toLowerCase()
  return browserLocale.startsWith('vi') ? 'vi' : DEFAULT_LOCALE
}

function interpolate(template: string, values?: TranslationValues): string {
  if (!values) return template

  return template.replace(/\{(\w+)(?:,\s*plural,\s*one\s*\{([^{}]*)\}\s*other\s*\{([^{}]*)\})?\}/g, (_, key: string, one: string | undefined, other: string | undefined) => {
    const value = values[key]
    if (one !== undefined && other !== undefined) {
      return Number(value) === 1 ? one : other
    }
    return value === null || value === undefined ? '' : String(value)
  })
}

export function translate(locale: Locale, key: TranslationKey, values?: TranslationValues): string {
  return interpolate(messages[locale][key] ?? messages[DEFAULT_LOCALE][key] ?? key, values)
}

const defaultContext: LocaleContextValue = {
  locale: DEFAULT_LOCALE,
  setLocale: () => undefined,
  t: (key, values) => translate(DEFAULT_LOCALE, key, values),
}

const LocaleContext = createContext<LocaleContextValue>(defaultContext)

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(detectInitialLocale)

  useEffect(() => {
    try {
      window.localStorage.setItem(LOCALE_STORAGE_KEY, locale)
    } catch {
      // Locale still works for the current session even if persistence fails.
    }
    document.documentElement.lang = locale
  }, [locale])

  const value = useMemo<LocaleContextValue>(() => ({
    locale,
    setLocale: setLocaleState,
    t: (key, values) => translate(locale, key, values),
  }), [locale])

  return (
    <LocaleContext.Provider value={value}>
      {children}
    </LocaleContext.Provider>
  )
}

export function useI18n(): LocaleContextValue {
  return useContext(LocaleContext)
}

